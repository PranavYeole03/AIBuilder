import { ArrowLeft, Check, Plus, Rocket, Share2, Trash2 } from 'lucide-react'
import React, { useEffect, useState } from 'react'
import { motion } from "motion/react"
import { useSelector } from 'react-redux'
import { useNavigate } from 'react-router-dom'
import { serverUrl } from '../App'
import axios from 'axios'

const DashBoard = () => {
  const { userData } = useSelector(state => state.user)
  const [websites, setWebsites] = useState([])
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState("")
  const [copiedId, setCopiedId] = useState(null)
  const [deletingId, setDeletingId] = useState(null)

  const navigate = useNavigate()

  const handleDeploy = async (id) => {
    try {
      const result = await axios.get(`${serverUrl}/api/website/deploy/${id}`, { withCredentials: true })
      window.open(result.data.url, "_blank")
      setWebsites(prev =>
        prev.map(w =>
          w._id === id ? { ...w, deployed: true, deployUrl: result.data.url } : w
        )
      )
    } catch (error) {
      console.log(error)
    }
  }

  const handleDelete = async (id) => {
    try {
      await axios.delete(`${serverUrl}/api/website/delete/${id}`, {
        withCredentials: true
      })
      setWebsites(prev => prev.filter(w => w._id !== id))
    } catch (error) {
      console.log(error)
    }
  }

  useEffect(() => {
    const handleGetAllWebsites = async () => {
      setLoading(true)
      try {
        const result = await axios.get(`${serverUrl}/api/website/get-all`, { withCredentials: true })
        setWebsites(result.data || [])
        setLoading(false)
      } catch (error) {
        setLoading(false)
        setError(error.response?.data?.message)
      }
    }
    handleGetAllWebsites()
  }, [])

  const handleCopy = async (site) => {
    await navigator.clipboard.writeText(site.deployUrl)
    setCopiedId(site._id)
    setTimeout(() => setCopiedId(null), 2000)
  }

  return (
    <div className='min-h-screen bg-[#050505] text-white'>
      <div className='sticky top-0 z-40 backdrop-blur-xl bg-black/50 border-b border-white/10'>
        <div className='max-w-7xl mx-auto px-6 h-16 flex items-center justify-between'>
          <div className='flex items-center gap-4'>
            <button className='p-2 rounded-lg hover:bg-white/10 transition' onClick={() => navigate("/")}>
              <ArrowLeft size={20} />
            </button>
            <h1 className='text-lg font-semibold'>DashBoard</h1>
          </div>

          <button
            className='px-4 py-2 rounded-lg bg-white text-black font-semibold hover:scale-105 transition flex items-center gap-1'
            onClick={() => navigate("/generate")}
          >
            <Plus size={18} /> New Website
          </button>
        </div>
      </div>

      <div className='max-w-7xl mx-auto px-6 py-10'>

        <motion.div initial={{ opacity: 0, y: 12 }} animate={{ opacity: 1, y: 0 }} className='mb-10'>
          <p className='text-sm text-zinc-400 mb-1'>Welcome Back</p>
          <h1 className='text-3xl font-bold'>{userData.name}</h1>
        </motion.div>

        {!loading && !error && websites.length === 0 && (
          <div className='mt-24 text-center text-zinc-200'>You have not created any websites yet</div>
        )}

        {!loading && !error && websites.length > 0 && (
          <div className='grid grid-cols-1 sm:grid-cols-2 xl:grid-cols-3 gap-8'>
            {websites.map((w, i) => {
              const copied = copiedId === w._id

              return (
                <motion.div
                  key={w._id}
                  initial={{ opacity: 0, y: 20 }}
                  animate={{ opacity: 1, y: 0 }}
                  transition={{ delay: i * 0.05 }}
                  whileHover={{ y: -6 }}
                  onClick={() => navigate(`/editor/${w._id}`)}
                  className='rounded-2xl bg-white/5 border border-white/10 overflow-hidden hover:bg-white/10 transition flex flex-col'
                >
                  <div className='relative h-40 bg-black'>
                    <iframe srcDoc={w.latestCode || ""} className='absolute inset-0 w-[140%] h-[140%] scale-[0.72] origin-top-left pointer-events-none bg-white' />
                  </div>

                  <div className='p-5 flex flex-col gap-4 flex-1'>

                    <h3 className='text-base font-semibold'>{w.title}</h3>
                    <p className='text-xs text-zinc-400'>Last Updated {""}
                      {new Date(w.updatedAt).toLocaleDateString()}
                    </p>

                    <div className="mt-auto flex gap-2">

                      {!w.deployed ? (
                        <button
                          onClick={(e) => {
                            e.stopPropagation()
                            handleDeploy(w._id)
                          }}
                          className='flex-1 flex items-center justify-center gap-2 px-4 py-2 rounded-xl text-sm font-semibold bg-linear-to-r from-indigo-500 to-purple-500'
                        >
                          <Rocket size={18} /> Deploy
                        </button>
                      ) : (
                        <motion.button
                          whileTap={{ scale: 0.95 }}
                          onClick={(e) => {
                            e.stopPropagation()
                            handleCopy(w)
                          }}
                          className={`flex-1 flex items-center justify-center gap-2 px-4 py-2 rounded-xl text-sm
                          ${copied ? "bg-emerald-500/20 text-emerald-400" : "bg-white/10"}`}
                        >
                          {copied ? <><Check size={14} /> Copied</> : <><Share2 size={14} /> Share</>}
                        </motion.button>
                      )}

                      {deletingId === w._id ? (
                        <div className="flex gap-2">
                          <motion.button
                            whileTap={{ scale: 0.95 }}
                            onClick={(e) => {
                              e.stopPropagation()
                              handleDelete(w._id)
                              setDeletingId(null)
                            }}
                            className="px-3 py-2 rounded-lg text-xs bg-red-500 text-white"
                          >
                            Confirm
                          </motion.button>

                          <motion.button
                            whileTap={{ scale: 0.95 }}
                            onClick={(e) => {
                              e.stopPropagation()
                              setDeletingId(null)
                            }}
                            className="px-3 py-2 rounded-lg text-xs bg-white/10 text-zinc-300"
                          >
                            Cancel
                          </motion.button>
                        </div>
                      ) : (
                        <motion.button
                          whileHover={{ scale: 1.05 }}
                          whileTap={{ scale: 0.9 }}
                          onClick={(e) => {
                            e.stopPropagation()
                            setDeletingId(w._id)
                          }}
                          className="p-3 rounded-xl bg-red-500/10 hover:bg-red-500/20 text-red-400 border border-red-500/20 transition"
                        >
                          <Trash2 size={16} />
                        </motion.button>
                      )}

                    </div>
                  </div>
                </motion.div>
              )
            })}
          </div>
        )}
      </div>
    </div>
  )
}

export default DashBoard